#This program compute and print the area and circumference of the circle when
#assigned a value for the diameter of a circle.

#Declaring Variable
diameter = 12
radius = diameter/2

#Computing area and circumference
area = 3.14*(radius**2)
circumference = 2*3.14*radius

#Output
print("The area of the circle is :", area)
print("The circumference of the circle is :", circumference)





